    private void validarCampoObrigatorio() throws Exception{
        
        if (txtNome.getText().isEmpty()) {
            throw new Exception("Nome não pode ser vazio!");
        }
        
        if (txtNumero.getText().isEmpty() && txtCelular.getText().isEmpty()) {
            throw new Exception("É necessário informar um número de telefone ou celular!");
        }
    }
    
    public ArrayList<EnderecoClienteDto> getEnderecoCliente () {
        
        ArrayList<EnderecoClienteDto> vEnderecos = new ArrayList<EnderecoClienteDto>();
        EnderecoClienteDto oEndereco = new EnderecoClienteDto();
        
        oEndereco.setRua(txtEndereco.getText());
        oEndereco.setNumero(txtNumero.getText());
        oEndereco.setBairro(txtBairro.getText());
        oEndereco.setCidade(txtCidade.getText());
        
        vEnderecos.add(oEndereco);
        
        return vEnderecos;
        
    }
    
    private void salvar() {
        
        ClienteVO oCliente = new ClienteVO();
        
        oCliente.setNome(txtNome.getText());
        oCliente.setEmail(txtEmail.getText());
        oCliente.setTelefone(txtTelefone.getText());
        oCliente.setvEndereco(getEnderecoCliente());
        
        oClienteController.salvar(oCliente);
        
    }

